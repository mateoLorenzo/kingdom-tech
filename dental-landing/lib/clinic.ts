type ClinicId = keyof typeof registry;

function resolveClinicId(): ClinicId {
  const raw = process.env.NEXT_PUBLIC_CLINIC_ID;
  if (raw && raw in registry) {
    return raw as ClinicId;
  }
  return "dental";
}

export const clinic: ClinicConfig = registry[resolveClinicId()];

export function whatsappUrl(message?: string): string {
  const { phone, defaultMessage } = clinic.contact.whatsapp;
  const text = message ?? defaultMessage;
  return `https://wa.me/${phone}?text=${encodeURIComponent(text)}`;
}

// Primary CTA URL. Defaults to WhatsApp; clinics with `contact.mode === "phone"`
// (e.g. they explicitly don't use WhatsApp) fall back to a `tel:` link built
// from `contact.whatsapp.phone`.
export function ctaUrl(message?: string): string {
  if (clinic.contact.mode === "phone") {
    // `callTel` lets clinics override the exact tel: target (e.g. 0800 numbers
    // that shouldn't get a +country-code prefix). Otherwise build from the phone.
    return clinic.contact.callTel
      ? `tel:${clinic.contact.callTel}`
      : `tel:+${clinic.contact.whatsapp.phone}`;
  }
  return whatsappUrl(message);
}

export const usesWhatsapp = clinic.contact.mode !== "phone";

export function resolveHref(link: {
  href?: string;
  whatsapp?: boolean;
}): string {
  if (link.whatsapp) return ctaUrl();
  return link.href ?? "#";
}
